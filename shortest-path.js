/**
 * @param {number[][]} grid
 * @param {number} k
 * @return {number}
 */
var shortestPath = function (grid, k) {
  let shortest;
  if (grid.length === 1 && grid[0].length === 1) return 0;
  let counter = -1;
  const traverse = (y, x, k, left, up) => {
    if (shortest) return;
    counter++;

    if (y === grid.length - 1 && x === grid[0].length - 1)
      return (shortest = counter);
    if (y + 1 <= grid.length - 1 && grid[y + 1][x] !== 2) {
      //down
      if (grid[y + 1][x] === 0) {
        grid[y + 1][x] = 2;
        traverse(y + 1, x, k, left, up);
        grid[y + 1][x] = 0;
      }
      if (grid[y + 1][x] === 1 && k > 0) {
        grid[y + 1][x] = 2;
        traverse(y + 1, x, k - 1, left, up);
        grid[y + 1][x] = 1;
      }
    }
    if (x + 1 <= grid[0].length - 1 && grid[y][x + 1] !== 2) {
      if (grid[y][x + 1] === 0) {
        grid[y][x + 1] = 2;
        traverse(y, x + 1, k, left, up);
        grid[y][x + 1] = 0;
      }
      if (grid[y][x + 1] === 1 && k > 0) {
        grid[y][x + 1] = 2;
        traverse(y, x + 1, k - 1, left, up);
        grid[y][x + 1] = 1;
      }
    }
    if (left && x - 1 >= 0 && grid[y][x - 1] !== 2) {
      if (grid[y][x - 1] === 0) {
        grid[y][x - 1] = 2;
        traverse(y, x - 1, k, left, up);
        grid[y][x - 1] = 0;
      }
      if (grid[y][x - 1] === 1 && k > 0) {
        grid[y][x - 1] = 2;
        traverse(y, x - 1, k - 1, left, up);
        grid[y][x - 1] = 1;
      }
    }
    if (up && y - 1 >= 0 && grid[y - 1][x] !== 2) {
      if (grid[y - 1][x] === 0) {
        grid[y - 1][x] = 2;
        traverse(y - 1, x, k, left, up);
        grid[y - 1][x] = 0;
      }
      if (grid[y - 1][x] === 1 && k > 0) {
        grid[y - 1][x] = 2;
        traverse(y - 1, x, k - 1, left, up);
        grid[y - 1][x] = 1;
      }
    }
    counter--;
  };

  const holder = k;
  while (k >= 0) {
    traverse(0, 0, k, false, false);
    if (shortest) return shortest;
    k--;
  }
  k = holder;
  while (k >= 0) {
    traverse(0, 0, k, false, true);
    if (shortest) return shortest;
    k--;
  }
  k = holder;
  while (k >= 0) {
    traverse(0, 0, k, true, false);
    if (shortest) return shortest;
    k--;
  }
  k = holder;
  while (k >= 0) {
    traverse(0, 0, k, true, true);
    if (shortest) return shortest;
    k--;
  }
  return -1;
};

